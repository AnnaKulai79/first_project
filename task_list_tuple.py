def roman_num(num):
    num = num[::-1]
    num_dict = {'I': 1, 'V': 5, 'X': 10, 'L': 50, 'C': 100, 'D': 500, 'M': 1000}
    non_repeatable = {'V', 'L', 'D'}   
    res = num_dict[num[0]]
    count_rep = 0
    for i in range(1, len(num)):
        if (count_rep > 0 and num[i] in non_repeatable) or \
            (num_dict.get(num[i]) == None) or \
            (num[i - 1] == num[i] and count_rep >= 3) or \
            (num[i-1] == 'I' and num[i] not in ('V','X')) or \
            (num[i-1] == 'X' and num[i] not in ('L','C')) or \
            (num[i-1] == 'C' and num[i] not in ('D','M')) or \
            (num[i-1] in ('V','L','D')):
            return "Invalid number"
        elif num[i - 1] == num[i]:
            count_rep +=1
        else:
            count_rep =0
        if num_dict[num[i]] < num_dict[num[i - 1]]:
            res -= num_dict[num[i]]
        else:
            res += num_dict[num[i]]
    return res
rom_num = input("Enter roman number(num <= 4000): ")
